<script>
  var tl = new TimelineLite();
  tl.staggerFrom(".menu-list li", 0.4, {autoAlpha:0, cycle: {
                  x: [-100, 100]
              }, ease:Power1.easeOut}, 0.07);

  $('.btn-menu').hover(
    function() {
      $('.menu-list').fadeIn();
      tl.play();
    },
    function() {
      $('.menu-list').fadeOut();
      tl.reverse();
    }
  );

  (function() {
    var $columnAccordian = document.querySelector('.column-accordian'),
      items = [].slice.call($columnAccordian.querySelectorAll('.column')),
      prev2, prev = $columnAccordian.querySelector('.flex');

    items.forEach(
      function(item) {
        item.addEventListener('click', function() {
          if (prev !== item) {
            prev.classList.remove('flex');
            item.classList.add('flex');
            prev2 = prev;
            prev = item;
          } else {
            prev2.classList.add('flex');
            item.classList.remove('flex');
            prev = prev2 || prev;
            prev2 = item;
          }
        });
      });
  })();
</script>
